    private Books getInput() {
        id = textFieldBookID.getText();
        title = textFieldTitle.getText();
        String author = textFieldAuthor.getText();
        String publisher = textFieldPublisher.getText();
        price = Integer.parseInt(textFieldPrice.getText());
        stock = Integer.parseInt(textFieldStock.getText());
        
        book = new Books(id, title, author, publisher, price, stock);
        return book;
    }

