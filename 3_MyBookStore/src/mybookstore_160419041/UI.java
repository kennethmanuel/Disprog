    private void tableDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDisplayMouseClicked
        int row = tableDisplay.getSelectedRow();
        textFieldBookID.setText(String.valueOf(tableDisplay.getValueAt(row, 0)));
        textFieldTitle.setText(String.valueOf(tableDisplay.getValueAt(row, 1)));
        textFieldAuthor.setText(String.valueOf(tableDisplay.getValueAt(row, 2)));
        textFieldPublisher.setText(String.valueOf(tableDisplay.getValueAt(row, 3)));
        textFieldPrice.setText(String.valueOf(tableDisplay.getValueAt(row, 4)));
    }//GEN-LAST:event_tableDisplayMouseClicked

    public void refreshtable() {
        DefaultTableModel model = (DefaultTableModel) tableDisplay.getModel();
        model.setRowCount(0);
        Object[] rowData = new Object[6];
        for (int i = 0; i < book.display().size(); i++) {
            rowData[0] = book.display().get(i).getBookid();
            rowData[1] = book.display().get(i).getTitle();
            rowData[2] = book.display().get(i).getAuthor();
            rowData[3] = book.display().get(i).getPublisher();
            rowData[4] = book.display().get(i).getPrice();
            rowData[5] = book.display().get(i).getStock();
            model.addRow(rowData);
        }

    }

